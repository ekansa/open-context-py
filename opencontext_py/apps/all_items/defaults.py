def is_valid_uuid(val):
    try:
        return GenUUID.UUID(str(val))
    except ValueError:
        return None


def verify_manifest_uuids(dict_list=DEFAULT_MANIFESTS):
    """Verifies deterministically created uuids"""
    check_types = (
        URI_CONTEXT_PREFIX_ITEM_TYPES
        + ['vocabularies']
    )
    for man_dict in dict_list:
        uuid = man_dict['uuid']
        uri = man_dict.get('uri')
        if man_dict['item_type'] not in check_types:
            # Not an item type that has a
            # deterministic uuid
            continue
        ex_uuid = AllManifest().primary_key_create(
            item_type=man_dict['item_type'],
            project_id=man_dict['project_id'],
            context_id=man_dict['context_id'],
            uri=uri,
        )
        if not is_valid_uuid(uuid):
            raise ValueError(f'Not a valid config uuid: {uuid}')
        if not is_valid_uuid(ex_uuid):
            raise ValueError(f'Not a valid generated uuid: {ex_uuid}')
        if ex_uuid == uuid:
            continue
        print(f'{uri} should have {ex_uuid}')


def load_resource_and_resource_types():
    res_man_obj, _ = AllManifest.objects.get_or_create(
        uuid=DEFAULT_RESOURCES_UUID,
        defaults=DEFAULT_RESOURCES_DICT
    )
    for rt_uuid in OC_RESOURCE_TYPES_UUIDS:
        rt_obj = AllManifest.objects.filter(uuid=rt_uuid).first()
        if not rt_obj:
            print(f'Child resource type {rt_uuid} is missing')
            continue
        assert_dict = {
            'project_id': OPEN_CONTEXT_PROJ_UUID,
            'publisher_id': OPEN_CONTEXT_PUB_UUID,
            'source_id': DEFAULT_SOURCE_ID,
            'subject_id': rt_obj.uuid,
            'predicate_id': PREDICATE_RDFS_SUB_CLASS_OF_UUID,
            'object_id': res_man_obj.uuid,
        }
        ass_obj, _ = AllAssertion.objects.get_or_create(
            uuid=AllAssertion().primary_key_create(
                subject_id=assert_dict['subject_id'],
                predicate_id=assert_dict['predicate_id'],
                object_id=assert_dict['object_id'],
            ),
            defaults=assert_dict
        )
        print(
            f'Assertion {ass_obj.uuid}: '
            f'is {ass_obj.subject.label} [{ass_obj.subject.uuid}]'
            f'-> {ass_obj.predicate.label} [{ass_obj.predicate.uuid}]'
            f'-> {ass_obj.object.label} [{ass_obj.object.uuid}]'
        )


def load_default_entities(
    man_dict_list=DEFAULT_MANIFESTS,
    id_dict_list=DEFAULT_IDENTIFIERS,
    assert_dict_list=DEFAULT_ASSERTIONS,
    ):
    """Loads default, required by Open Context """
    fk_fields = [
        'publisher_id',
        'project_id',
        'item_class_id',
        'context_id'
    ]
    for man_dict in man_dict_list:
        uuid = man_dict['uuid']
        if not is_valid_uuid(uuid):
            raise ValueError(f'Not a valid config uuid: {uuid}')
        m, _ = AllManifest.objects.get_or_create(
            uuid=uuid,
            defaults=man_dict
        )
        print(f'Item {m.uuid}, {m.label} created.')

    for man_dict in man_dict_list:
        uuid = man_dict['uuid']
        m = AllManifest.objects.get(uuid=uuid)
        for fk_field in fk_fields:
            fk_val = man_dict.get(fk_field)
            if not fk_val:
                # This foreign key field is not set
                continue
            setattr(m, fk_field, fk_val)

        print(f'Updating related for {m.label}')
        m.save()

    for id_dict in id_dict_list:
        uuid = AllIdentifier().primary_key_create(
            item_id=id_dict['item_id'],
            scheme=id_dict['scheme'],
        )
        id_obj, _ = AllIdentifier.objects.get_or_create(
            uuid=uuid,
            defaults=id_dict
        )
        print(
            f'ID obj {id_obj.uuid}: '
            f'{id_obj.item.label} ({id_obj.item.uuid}) -> '
            f'{id_obj.id} ({id_obj.scheme}) created '
        )

    for assert_dict in assert_dict_list:
        ass_obj, _ = AllAssertion.objects.get_or_create(
            uuid=AllAssertion().primary_key_create(
                subject_id=assert_dict['subject_id'],
                predicate_id=assert_dict['predicate_id'],
                object_id=assert_dict['object_id'],
            ),
            defaults=assert_dict
        )
        print(
            f'Assertion {ass_obj.uuid}: '
            f'is {ass_obj.subject.label} [{ass_obj.subject.uuid}]'
            f'-> {ass_obj.predicate.label} [{ass_obj.predicate.uuid}]'
            f'-> {ass_obj.object.label} [{ass_obj.object.uuid}]'
        )
    load_resource_and_resource_types()


